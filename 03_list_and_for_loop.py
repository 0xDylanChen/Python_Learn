# 檔名: 03_list_and_for_loop.py
# 目的: 學習 list (列表) 和 for 迴圈

print("--- 我的水果籃 ---")

# 建立一個叫做 my_fruits 的列表
# 裡面放了一些水果的名字 (字串)
my_fruits = ["蘋果", "香蕉", "橘子", "草莓", "西瓜"]

print("我的水果籃裡有以下水果:")
print("--------------------------------")

# 這就是 for 迴圈
# 它會從 my_fruits 列表裡，一次拿一個東西出來
# 然後把拿出來的東西暫時叫做 'fruit'
# 迴圈會一直跑到列表裡的東西都拿完為止
for fruit in my_fruits:
    # 印出目前拿到的水果
    print("我有一個 " + fruit)

print("--------------------------------")
print("水果都拿完囉！")

